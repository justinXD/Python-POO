# Evalua si es mayor que y devuelve true o false
print(1 > 2)

# Evalua si es menor que y devuelve true o false
print(1 < 2)

# Evalua si es mayor o igual que y devuelve true o false
print(1 >= 2)

# Evalua si es menor o igual que y devuelve true o false
print(1 <= 2)

# Evalua si es igual a y devuelve true o false
print(1 == 2)

# Evalua si es igual a y devuelve true o false, aqui como el tipo de dato es diferente devuelve False
print(1 == '1')

# Evalua si es diferente y devuelve true o false
print(1 != 2)
